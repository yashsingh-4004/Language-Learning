// Methods to change Array

// 1. push and pop method
// 2. shift and unshift method


// 1. push and pop method

// Push (adds an element at last of an Array) 

let fruits1=["mango","papaya","grapes"];

fruits1.push("banana");

console.log(fruits1);



// Pop (Removes the last element from an Array and also returns the removed item)\

let fruits2=["grapes", "apple", "melon"];

let poppedfruit=fruits2.pop();

console.log(poppedfruit);





// Unshift and Shift method

// 1. Unshift (Adds an element on starting of an array)

let fruits3=["pomogranade", "pineapple", "banana"];

fruits3.unshift("apple");

console.log(fruits3);



// 2. Shift (Removes an element in an array from starting)

let fruits4=["mango","banana","apple"];

let shiftfruit=fruits4.shift();

console.log(shiftfruit);