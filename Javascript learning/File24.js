// Break 

for(let i=0; i<=10; i++)
{
    if(i==5)
    {
        break;
    }
    console.log(i);
}


// Break keyword ends the loop on the specific loop




// Continue

for(let j=0; j<=10; j++)
{
    if(j==5)
    {
        continue;
    }

    console.log(j);

}
// Continue keyword skips the loop on a specific loop
