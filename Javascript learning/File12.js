// Comparison oprators

// Comparison oprators is used to Compare 2 Variables Values

// Some Comparison Oprators are:

// 1. < 
// 2. >
// 3. <=
// 4. >=
// 5. ==
// 6. ===
// 7. !=
// 8. !==



let num1=20;
let num2=10;

console.log(num1<num2);
console.log(num1>num2);
console.log(num1<=num2);
console.log(num1>=num2);
console.log(num1==num2);
console.log(num1!=num2);




// == vs ===

// 1. == oprator compares only the value inside the variable

let num3=10;
let num4=10;

console.log(num3==num4);


// 2. === oprator compares the Value inside the variable and also the Datatype of the variable

let num5=10;
let num6="10";

console.log(num5===num6);



// != vs !==

// 1. != oprator compares only the value inside the variable

let num7=20;
let num8="30";

console.log(num7!=num8);


// 2.!== oprator compares the Value inside the variable and also the Datatype of the variable

let num9=20;
let num10="30";

console.log(num9!==num10);
